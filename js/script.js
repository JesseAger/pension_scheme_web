// for loop
for (var i = 0; i < 5; i++) {
  console.log(i);
}

// while loop
var count = 0;
while (count < 5) {
  console.log(count);
  count++;
}

// do-while loop
var num = 0;
do {
  console.log(num);
  num++;
} while (num < 5);


